func largestProductFor(seriesLength: Int) -> Int {
    (0..<(digits.count - seriesLength))
        .map { startIndex -> Int in
            let start = digits.index(digits.startIndex, offsetBy: startIndex)
            let end = digits.index(digits.startIndex, offsetBy: startIndex + seriesLength)
            return digits[start..<end].reduce(1) { (result, digitString) -> Int in
                result * Int(String(digitString))!
            }
    }.reduce(1, max)
}

